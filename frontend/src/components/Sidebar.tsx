import { NavLink } from 'react-router-dom'
import { useAuth } from '../hooks/useAuth'
import { useDarkMode } from '../hooks/useDarkMode'

const navItems = [
  { to: '/', label: 'Dashboard', icon: DocIcon },
  { to: '/ny', label: 'Nytt dokument', icon: PlusIcon },
]

const adminItems = [
  { to: '/admin', label: 'Admin', icon: CogIcon },
]

export default function Sidebar() {
  const { user, logout } = useAuth()
  const [dark, toggleDark] = useDarkMode()

  return (
    <aside className="w-64 h-screen bg-white dark:bg-gray-900 border-r border-gray-200 dark:border-gray-800 flex flex-col">
      {/* Logo */}
      <div className="px-5 py-4 border-b border-gray-200 dark:border-gray-800">
        <img
          src="/logo-tekstflyt-light.png"
          alt="TekstFlyt"
          className="h-9 dark:hidden"
        />
        <img
          src="/logo-tekstflyt-dark.png"
          alt="TekstFlyt"
          className="h-9 hidden dark:block"
        />
        <p className="mt-1.5 text-xs text-gray-700 dark:text-gray-300 tracking-wide">
          Kulde- &amp; Varmepumpeteknikk AS
        </p>
      </div>

      {/* Navigation */}
      <nav className="flex-1 px-3 py-4 space-y-1">
        {navItems.map((item) => (
          <NavLink
            key={item.to}
            to={item.to}
            end={item.to === '/'}
            className={({ isActive }) =>
              `flex items-center gap-3 px-3 py-2 rounded-lg text-sm font-medium transition-colors ${
                isActive
                  ? 'bg-kvtas-50 text-kvtas-700 dark:bg-kvtas-900/30 dark:text-kvtas-400'
                  : 'text-gray-700 dark:text-gray-300 hover:bg-gray-100 dark:hover:bg-gray-800'
              }`
            }
          >
            <item.icon />
            {item.label}
          </NavLink>
        ))}

        {user?.role === 'admin' &&
          adminItems.map((item) => (
            <NavLink
              key={item.to}
              to={item.to}
              className={({ isActive }) =>
                `flex items-center gap-3 px-3 py-2 rounded-lg text-sm font-medium transition-colors ${
                  isActive
                    ? 'bg-kvtas-50 text-kvtas-700 dark:bg-kvtas-900/30 dark:text-kvtas-400'
                    : 'text-gray-700 dark:text-gray-300 hover:bg-gray-100 dark:hover:bg-gray-800'
                }`
              }
            >
              <item.icon />
              {item.label}
            </NavLink>
          ))}
      </nav>

      {/* Footer: dark mode toggle + user info */}
      <div className="px-3 py-4">
        <div className="flex justify-end px-3">
          <button
            onClick={toggleDark}
            className="flex items-center justify-center w-8 h-8 rounded-lg text-gray-500 dark:text-gray-400 hover:bg-gray-100 dark:hover:bg-gray-800 transition-colors cursor-pointer"
          >
            {dark ? <SunIcon /> : <MoonIcon />}
          </button>
        </div>

        <hr className="my-3 border-gray-200 dark:border-gray-800" />

        <div className="flex items-center justify-between px-3">
          <span className="text-sm font-medium text-gray-900 dark:text-gray-100 truncate">
            {user?.display_name}
          </span>
          <button
            onClick={() => logout()}
            className="text-xs text-gray-500 hover:text-gray-700 dark:text-gray-400 dark:hover:text-gray-200 cursor-pointer"
          >
            Logg ut
          </button>
        </div>
      </div>
    </aside>
  )
}

